function Language(lang) {
    var __construct = function() {
        if (eval('typeof ' + lang) == 'undefined') {
            lang = "vi";
        }
        return;
    }
    
    this.getValue = function(str, defaultStr) {
        var retStr = eval('eval(lang).' + str);
        if (typeof retStr != 'undefined') {
            return retStr;
        } else if (typeof defaultStr != 'undefined') {
            return defaultStr;
        } else {
            return eval('en.' + str);
        }
    }
}

